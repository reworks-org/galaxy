--[[ These are sample scripts that can be run by [Corona SDK engine](http://www.coronalabs.com/products/corona-sdk/).
 
- Make Corona simulator available in PATH or put it into one of the default folders: `C:\Program Files\Corona Labs\Corona SDK` or `D:\Program Files\Corona Labs\Corona SDK` on Windows or `/Applications` on Mac OS X.
- Switch to Corona interpreter by going to `Project | Lua Interpreter | Corona`.
- Open one of the following scripts:
  - [live-coding-simple](+live-coding-simple/main.lua): a simple live coding script
- Change the project folder to point to the directory of the script (for example, by going to `Project | Project Directory | Set From Current File`).
- Run the script using `Project | Run as Scratchpad`.
