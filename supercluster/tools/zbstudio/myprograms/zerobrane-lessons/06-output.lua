# Output window

In addition to having your program to **do** something, you may want to output something interesting. So, where does that output go? The output of the programs you run from this environment goes to the **output window**, which is right below the editor. In addition to displaying output messages of the programs you run, it also captures **errors** and additional messages you may get during execution of those programs.

Now run this script. You should see the lines printed in the `Output` window.
