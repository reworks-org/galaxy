--[[ These are sample scripts that can be run by [Gideros mobile engine](http://www.giderosmobile.com/).

- Make Gideros executable available in PATH or put it into one of the default folders: `C:\Program Files\Gideros` or `D:\Program Files\Gideros` on Windows or `/Applications` on Mac OS X.
- Switch to Gideros interpreter by going to `Project | Lua Interpreter | Gideros`.
- Open one of the following scripts:
  - [live-coding-simple](+live-coding-simple/main.lua): a simple live coding script
- Change the project folder to point to the directory of the script (for example, by going to `Project | Project Directory | Set From Current File`).
- Run the script using `Project | Run as Scratchpad`.
