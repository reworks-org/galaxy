# Program is a list of instructions

Every program is a list of instructions. These instructions "tell" the computer what to do. You will learn how to write these instructions in a way that computers can follow and other people can understand.

One example with a list of instructions that everyone is familiar with is a culinary recipe. It usually provides step-by-step instructions on what ingredients to use and what to do with them. The order is critical too: if you accidentally do steps out of order, the results may not be pleasant (or edible).
