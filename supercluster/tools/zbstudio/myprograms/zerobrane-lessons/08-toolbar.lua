# Toolbar

Some of the most frequently used commands in the editor are also available on the **toolbar** (which is right above the editor window and below the menu bar). There, you will find commands to open a new or an existing file, save a file, debugger commands, and commands to search and replace text in your programs. I am sure you have already figured by this time that you can activate those commands by clicking on the icons in the toolbar.
