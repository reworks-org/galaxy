# Editor

The window with the text you are reading is the editor. This is where you write all the commands you want to have in your program to make it do something awesome.

The editor allows you to open several programs or files and work on them at the same time. Each opened file has its own page with a name and a tab at the top of the page. You can **switch** between pages by clicking on those tabs. You can also **close** those pages when you don't need them by pressing `Ctrl-W` or using the `File | Close Page` menu.
