# Project files

As you keep working on your programs, you may end up having many of them, and decide to organize them based on what they do or what they are for. You may group them by the project they belong to. The **project** panel provides a way to access project files and to switch between projects. It is located on the left side of the editor window. 

The project panel helps you in several ways: it provides a bird's-eye view of all files in your project, it highlights the file you are working with (as long as it is in the same project), and it allows you to open a file by selecting a file and pressing `Enter` or double-clicking on it.
