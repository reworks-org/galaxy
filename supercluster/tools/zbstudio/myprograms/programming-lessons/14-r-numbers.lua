# Review: numbers, operations, and priority

**Number**: any series of digits; may start with a minus sign and include a decimal point. Addition, subtraction, multiplication, and division **operations** can be used in **expressions** to do math with numbers. Parentheses can be used to **group** operations when you need to enforce priority of operations.

Examples: |2| + (|3| - |4|); |2200| / |6| / |365|; |1.0045662100457|; |-5|; |+128|
